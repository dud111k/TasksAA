
/*
Напечатать числа следующим образом:
а) 10 10.4
11 11.4
...
25 25.4

б) 25 25.5 24.8
26 26.5 25.8
...
35 35.5 34.8
*/



using namespace std;

int main()
{
	// a)
	for (int i = 10; i <= 25; i++) {
		cout << i << " " << i + 0.4 << endl;
	}
	// б)
	for (int j = 25; j <= 35; j++) {
		cout << j << " " << j + 0.5 << " " << j - 0.2 << endl;
	}
	return 0;
}
